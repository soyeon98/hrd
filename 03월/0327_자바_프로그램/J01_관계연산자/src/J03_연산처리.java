public class J03_연산처리 {
    public static void main(String[] args) {
        // 산술 연산자 출력하기
        System.out.println("산술 연산자 출력하기____________");
        System.out.println("3 + 5 = " +(3+5));
        System.out.println("3 - 5 = " +(3-5));
        System.out.println("3 * 5 = " +(3*5));
        System.out.println("3 / 5 = " +(3/5));
        System.out.println("3 % 5 = " +(3%5));

        //실수 연산 출력하기
        //둘 중 하나라도 실수이면 결과도 실수로 출력된다.
        System.out.println("실수 연산자 출력하기____________");
        System.out.println("5.0 / 2.0 = " + (5.0 / 2.0));
        System.out.println("5 / 2.0 =  "+ (5 / 2.0) );
        System.out.println("5.0 / 2 = "+ (5.0 / 2) );
        System.out.println("19.0 / 4 = "+ (19.0 / 4) );
        System.out.println("5.0 * 4  ="+ (5.0 * 4) );
    }
}
